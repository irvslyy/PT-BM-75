     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $project = new Project;
       $project->project_category = $request->project_category;
       $project->brand_project = $request->brand_project;
       $project->location_project = $request->location_project;
       $project->hold_project = $request->hold_project;
       $project->type_project = $request->type_project;
       $project->worker_project = $request->worker_project;
       $project->revenue_project = $request->revenue_project;
       $project->deadline_project = $request->deadline_project;
       $project->save();

       return back();
    }

